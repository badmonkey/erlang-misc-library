%%%%% ------------------------------------------------------- %%%%%


valid_numbers() -> lists:seq(1,45).
reverse_data() -> lists:reverse( data() ).

offset_data() ->
    lists:foldl(
        fun({Id, Nums}, Acc) ->
            Acc ++ [{Id, xmaths:list_differences( lists:sort(Nums) ) }]
        end,
        [], data() ).

reverse_offset_data() -> lists:reverse( offset_data() ).


%%%%% ------------------------------------------------------- %%%%%


newmap() -> newmap(0).
        
newmap(Init) ->
    lists:foldl(
        fun(X, AMap) ->
            maps:put(X, Init, AMap)
        end,
        maps:new(), valid_numbers() ).


%%%%% ------------------------------------------------------- %%%%%


frequency() ->
    lists:foldl(
        fun({_, Nums}, Map) ->
            lists:foldl(
                fun(X, AMap) ->
                    maps:put(X, xmaps:get(X, AMap, 0) +1, AMap)
                end,
                Map, Nums)
        end,
        newmap(), data() ).


most_frequent() ->
    lists:reverse( lists:keysort(2, maps:to_list( frequency() ) ) ).
        

%%%%% ------------------------------------------------------- %%%%%


ages(X) ->
    { Result, _, _ } = lists:foldl(
        fun( {_, Nums}, { Ages, Cnt, Last} ) ->
            Sorted = lists:sort(Nums),
            case xlists:sorted_find(X, Sorted) of
                true    -> { Ages ++ [Cnt - Last], Cnt +1, Cnt }
            ;   false   -> { Ages, Cnt +1, Last }
            end
        end,
        { [], 1, 0 },
        reverse_data() ++ [{ignore, valid_numbers()}] ),
    Result.


last_ages() ->
    maps:from_list( [ { X, hd(ages(X)) } || X <- valid_numbers() ] ).


