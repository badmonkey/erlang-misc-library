draw_history(N) ->
    Draws = xlists:take(N, reverse_data()),
    Nums = lists:foldl(
        fun({_, Nums}, Acc) ->
            Acc ++ Nums
        end,
        [],
        Draws ),
    lists:sort( xlists:unique(Nums) ).
    

%%%%% ------------------------------------------------------- %%%%%


intersect_size(S1, S2) -> sets:size( sets:intersection(S1, S2) ).

results([_P1, _P2, _P3, _P4, _P5, _P6] = Picks, [N1, N2, N3, N4, N5, N6, Sup1, Sup2]) ->
    PicksSet = sets:from_list(Picks),
    NumsSet = sets:from_list([N1, N2, N3, N4, N5, N6]),
    SupSet = sets:from_list([Sup1, Sup2]),
    
    case { intersect_size(PicksSet, NumsSet), intersect_size(PicksSet, SupSet) } of
        { 6, _ }    -> div1
    ;   { 5, 1 }    -> div2
    ;   { 5, 0 }    -> div3
    ;   { 4, _ }    -> div4
    ;   { 3, 2 }    -> div5
    ;   { 3, 1 }    -> div5
    ;   { 2, 2 }    -> div6
    ;   { 1, 2 }    -> div6
    ;   _           -> none
    end.


