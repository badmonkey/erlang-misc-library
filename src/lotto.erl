first_offset() -> offset_slice(1).


offset_slice(1) ->
    lists:foldl(
        fun({_, [Hd|_]}, Acc) ->
            Acc ++ [Hd]
        end,
        [], offset_data() );

offset_slice(N) ->
    lists:foldl(
        fun({_, Nums}, Acc) ->
            Acc ++ [ lists:nth(N, Nums) ]
        end,
        [], offset_data() ).
        
offset_stats() ->
    [ xmaths:std_deviation2(offset_slice(N)) || N <- [1,2,3,4,5,6,7,8] ].

        
